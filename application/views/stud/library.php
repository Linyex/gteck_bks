<h3>Профориентационная работа</h3>
<p>Профориентация по книгам - необычный способ выбора профессии. Именно читая книги о деятельности и работе в той или иной отрасли, вы сможете больше узнать о профессии и понять, подходит ли она вам.</p>
<p>Есть один очень интересный способ профориентации: почитать книжки о профессиях.<br>
Это как будто бы:</p>
<ul>
    <li>ваша учительница ежедневно совершала авиарейсы с братом-летчиком, после чего докладывала вам, как проходил полет;</li>
    <li>в вашей семье 100 человек, и у всех разные профессии. С кем захотел, с тем и поговорил;</li>
    <li>профориентационный центр стал домом родным;</li>
    <li>вы за считанные дни провели на предприятии несколько десятков лет;</li>
    <li>вы ответили на тест, который не загоняет вас в рамки.</li>
</ul>
<p>Нравится профессия – добро пожаловать, вот карта, тут сказано, как действовать. А коли это не ваше – никаких вам цифр и ярлыков. Просто книгу почитали, просто вы другой. </p>
<p>Если у вас на примете нет специалиста, который даст совет, куда пойти учиться, такого человека заменят книги о профессии. Художественные, мемуарные, научно-популярные… Самые разные произведения способны дать информацию, необходимую для выбора.</p>
<p>Четыре совета для Вас:</p>
<ul>
    <li>читайте не только книги, где изображен человек в труде.</li>
    <li>обратите внимание: у некоторых профессионалов – следователей, например – настолько явно интересная жизнь, что о них пишут детективы (иногда с ходульными характерами).</li>
    <li>читая книги о профессионалах, сразу включайте профессиональное самосознание и представляйте себе, как бы поступил на месте изображаемого специалиста очень хороший специалист. То есть вы в будущем. </li>
    <li>книга всегда даст знание в системе. О профессионале в его труде год за годом. О карьере в развитии.</li>
</ul>
