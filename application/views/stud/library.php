<h3>Главная задача Библиотеки:</h3>
<p>Формирование, сохранение и предоставление пользователям наиболее полного фонда учебной, учебно-методической, справочной и научно-популярной литературы в соответствии с профилем учебного заведения.</p>
<p>Библиотека УО «Гомельский торгово–экономический колледж» Белкоопсоюза является учебно-вспомогательным и информационным структурным подразделением колледжа, организующим общественное пользование книгами и другими произведениями печати и носителями информации, составляющими фонд библиотеки.<br><br>

    Библиотека образована в 1944 году, как библиотека Гродненского кооперативного техникума, с июля 1953 года – библиотека Гомельского кооперативного техникума Белкоопсоюза, с 1966 года – библиотека Гомельского УПК « ПТУ-техникум» Белкоопсоюза, с 2000 года – библиотека УО «Гомельский торгово-экономический колледж» Белкоопсоюза.</p><br>
<h3>Основные направления деятельности библиотеки:</h3>
<ul>
    <li>Содействие администрации и педагогическому коллективу колледжа в организации учебного процесса;</li>
    <li>Обеспечение многообразия образовательного процесса всеми формами и методами библиотечного и информационно-библиографического обслуживания читателей, также качественным и оперативным комплектованием книжного фонда;</li>
    <li>Содействие успешному усвоению учебных программ, развитию творческого мышления, познавательных интересов и способностей учащихся;</li>
    <li>Содействие самообразованию учащихся и педагогических работников колледжа, обеспечение информацией их социальных и культурных потребностей;</li>
    <li>Воспитание у учащихся информационной культуры: привитие навыков умелого пользования книгой и другими источниками информации.</li>
</ul>
<br>
<p>
    Книжный фонд библиотеки предназначен для удовлетворения информационных потребностей читателей и включает 62616 экземпляров литературы, в т. ч. фонд учебной литературы составляет 47958 экземпляров, фонд учебно-методической литературы составляет 9986 экземпляров. Общий библиотечный фонд составляет 65898 экземпляров печатных изданий. Фонд периодических изданий библиотеки составляет около 3786 тыс. единиц хранения, среди которых – белорусские периодические издания экономического и общественно-политического профиля. Библиотека формирует фонд электронных изданий (энциклопедии, справочники, учебные пособия, сборники научно-методических материалов, периодических изданий и др.). Общая площадь библиотеки составляет 240 кв. м. Условно Библиотека делится на две зоны: зона книгохранения и зона обслуживания. Зона обслуживания (читальный зал и абонемент библиотеки) является самой значимой зоной – зоной обслуживания документами по требованиям читателей библиотек
</p>
