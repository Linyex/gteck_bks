            <p class="c-font-red-3">Первый секс: ошибок быть не должно!</p>
            <p>Что нужно сделать обязательно?</p>
            <ol>
                <li>
                    <p>Рассказать о правильных и, что не менее важно, неправильных способах контрацепции. Если верить опросам, то многие подростки убеждены в том, что календарный метод, прерванный половой акт и «Постинор» после полового акта - это нормальные способы контрацепции. Поэтому обязательно нужно рассказать о том, чем вредны эти способы.</p>
                    <p>Также среди подростков часто бытует миф, что оральный секс абсолютно безопасен и таким способом нельзя получить ЗППП. Недавно даже проводилось исследование, которое показало, что подростки предпочитают оральный секс именно из-за заблуждения, что он безопасен.</p>
                </li>
                <li>Показать, что вы не осуждаете интерес ребенка к сексу и продемонстрировать готовность обсуждать эту тему. Беременность и ЗППП - не единственные возможные последствия раннего начала половой жизни. Есть еще последствия психологические. Именно поэтому так важно не ругать и не наказывать подростка за то, что он уже занимается сексом. Именно к вам он должен придти, столкнувшись с проблемами, не боясь осуждения.</li>
                <li>Купить несколько книг, посвященных сексу и сексуальному воспитанию, чтобы они всегда были под рукой.</li>
                <li>Не запрещать дочери встречаться с мальчиками, а выражать сдержанный интерес и готовность помочь строить отношения с противоположным полом. Ей будет интересен ваш опыт и ваши советы, касающиеся отношений между мужчиной и женщиной. И если в этом вам удастся вызвать доверие, то и в вопросах секса она будет советоваться с вами.</li>
            </ol>
            <p>Конечно, очень важно предоставить исчерпывающую информацию о том, что касается секса, но не менее важно сформировать у дочери убеждение, что секс - это не показатель взрослости или крутости, а в первую очередь акт любви.</p>
            <p>За один день, неделю, даже месяц этого не сделаешь. С самого детства необходимо прививать ребенку такие ценности, как любовь, верность, искренность, и строить отношения в форме диалога, воспитывая доверие к родителям. И тогда в подростковом возрасте многих проблем просто не возникнет.</p>
            <p class="c-font-red-3">Родителям о табакокурении</p>
            <p>Табакокурение - одна из вреднейших привычек.</p>
            <p>Исследованиями выяснено, в чем вред табакокурения. В дыме табака содержится более 30 ядовитых веществ: никотин, углекислый газ, окись углерода, синильная кислота, аммиак, смолистые вещества, органические кислоты и др.</p>
            <p>Табачные изделия готовятся из высушенных листьев табака которые содержат белки, углеводы, минеральные соли, клетчатку, ферменты, жирные кислоты и др. Среди них важно отметить две группы веществ, опасных для человека, - никотин и изо-преноиды.</p>
            <p>Никотин относится к нервным ядам. Особенно вредно курение для детей u подростков. Еще не окрепшие нервная и кровеносная системы болезненно реагируют на табак.</p>
            <p>Статистические исследования показали, что у курящих людей часто встречаются раковые опухоли и других органов - пищевода, желудка, гортани, почек. У курящих трубку нередко возникает рак нижней губы вследствие канцерогенного действия экстракта, скапливающегося в мундштуке.</p>
            <p>Курящие подвергают опасности не только себя, но и окружающих людей; существует термин «Пассивное курение». В организме некурящих людей после пребывания в накуренном и непроветренном помещении определяется значительная концентрация никотина.</p>
