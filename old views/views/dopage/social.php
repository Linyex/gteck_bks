            <p class="c-font-red-3">Советы социального педагога родителям</p>
            <ol>
                <li>Проанализируйте вашу собственную систему ценностей в отношении воспитания детей. Способствует ли она одаренности и реализации личности в обществе?</li>
                <li>Будьте честными. Все дети весьма чувствительны ко лжи, а к одаренным детям это относится в большей степени.</li>
                <li>Оценивайте уровень развития ребенка.</li>
                <li>Избегайте длинных объяснений или бесед с ребенком.</li>
                <li>Старайтесь вовремя уловить изменения в ребенке. Они могут выражаться в неординарных вопросах или в поведении и являться признаком одаренности.</li>
                <li>Уважайте в ребенке индивидуальность. Не стремитесь проецировать на него собственные интересы и увлечения. Развивайте в своих детях следующие качества:
                    <ul>
                        <li>уверенность, базирующуюся на объективной самооценке;</li>
                        <li>понимание достоинств и недостатков в себе самом и окружающих;</li>
                        <li>интеллектуальную любознательность и готовность к исследовательскому риску;</li>
                        <li>уважение к доброте, честности, дружелюбию, сопереживанию, терпению, душевному мужеству;</li>
                        <li>привычку опираться на собственные силы и готовность нести ответственность за свои поступки;</li>
                        <li>умение находить общий язык и радость в общении с людьми всех возрастов.</li>
