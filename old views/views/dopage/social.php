<p>Учащиеся, их родители и преподаватели обращаются к педагогу социальному в трудные минуты своей жизни. Помочь словом и делом, содействовать тому, чтобы каждый из них поверил в себя, при необходимости пересмотрел свои жизненные ценности – главная задача педагога социального.</p>
<p>Значительное место в практике работы педагога социального занимает социальное посредничество – содействие разрешению возникших проблем в системе социальных взаимодействий подопечных, участие в поиске и мобилизации средств, подключении соответствующих специалистов и организаций для оказания необходимой помощи.</p>
<p>Социальный педагог организует работу с учащимися, нуждающимся в опеке и попечительстве.</p>
<p>В учреждении образования педагог социальный поддерживает партнерские взаимоотношения со всеми специалистами и структурными подразделениями по вопросам социально-педагогического сопровождения учебно-воспитательного процесса.</p>
<p>Вне учреждения образования для решения социально-педагогических проблем педагог социальный взаимодействует в установленном порядке с другими учреждениями образования, иными организациями и частными лицами.</p>
<h3>Социальный педагог:</h3>
<ul>
    <li>Защищает права и интересы учащихся.</li>
    <li>Обеспечивает социально-педагогическую поддержку и помощь семье в развитии и воспитании ребенка, выступает их представителем в учреждении образования, различных инстанциях, привлекая в этих целях других специалистов</li>
    <li>Ведет работу с семьями социального риска</li>
    <li>Организует учет учащихся, которые испытывают трудности в социализации, нуждаются в опеке (попечительстве), находятся в экстремальных ситуациях и принимает адекватные меры по оказанию им социально-педагогической помощи и поддержки</li>
    <li>Осуществляет работу по профилактике и предупреждению среди учащихся правонарушений, преступлений, пьянства, токсикомании, наркомании, СПИДа и другое</li>
    <li>Пропагандирует и содействует внедрению среди учащихся и их семей здорового образа жизни.</li>
    <li>Обеспечивает социально-педагогическую помощь и поддержку учащимся с особенностями умственного и физического развития и способствует их социальной адаптации в обществе</li>
    <li>Содействует становлению и развитию у учащихся общечеловеческих ценностей, общественно регулируемых норм и правил поведения в обществе, внутренней потребности в самовоспитании и самоконтроле</li>
    <li>Оказывает помощь учащимся в организации различных видов социально значимой деятельности, свободного времени, социальном и профессиональном самоопределении, трудоустройстве, оздоровлении</li>
    <li>Оказывает консультационную социально-педагогическую помощь родителям, педагогам, учащимся</li>
    <li>Взаимодействует в решении социально-педагогических проблем с органами власти и местного самоуправления, заинтересованными ведомствами и учреждениями, общественными организациями</li>
</ul>
