            <p class="c-font-red-3">Что не делать ни в коем случае?</p>
            <p>Не паниковать и не бояться. Молодежные субкультуры существовали в том или ином виде всегда. Причем не только для того, чтобы взрослых позлить. Неформальные объединения, подчеркнем еще раз, нужны молодежи для общения, самовыражения, развития.</p>
            <p>Не запрещать. Запретный плод, как известно, сладок. С неформалами всегда пытались бороться, но результат был нулевым либо обратным. Вспомним 60-е - 80-е годы прошлого века с их гонениями на хиппи, панков, металлистов... Эти субкультуры и по сей день никуда не делись, а их "гонители" и "запретители" давно потеряли авторитет.</p>
            <p>Не игнорировать. Это может оказаться еще хуже запретов. Пусть подросток знает, что вы в курсе его увлечений: что-то вас устраивает, что-то - нет. Иначе подросток может подумать, что он вам безразличен, что вы его разлюбили.</p>
            <p>Не оскорблять кумиров подростка. В этом возрасте даже фраза "Выключи эту ерунду (какофонию, рычалку)" может вызвать обиду, и как следствие - взрыв агрессии. Да-да, подросток может это воспринять не как ваше желание отдохнуть от грохота музыки, а как неуважение к себе, как желание оскорбить и задеть. Лучше сначала поинтересоваться, что это за группа, и лишь потом сказать, что вы устали, и попросить сделать звук потише.</p>
            <p>Не обвинять во всех смертных грехах. Высказывания из серии "Все неформалы - преступники и алкоголики" не только обидны, но еще и неверны. Пагубные привычки и преступления не зависят от любимой музыки и внешнего вида.</p>
            <p>Не преуменьшать важности подросткового увлечения: мол, это у тебя скоро пройдет, перерастешь, перебесишься. Даже если это верно, подросток все равно вам не поверит и будет настаивать, что "это - навсегда". Кстати, некоторые подростковые хобби и увлечения в будущем могут перерасти в профессию. Ведь кто знает, может быть, именно ваш ребенок станет в будущем пиар - агентом музыкальной группы или тренером спортивной команды. Даже если и нет, многие и во взрослом состоянии не бросают юношеские увлечения: слушают любимую музыку, болеют за любимую команду.</p>
            <p>Приняв эти несложные правила, вы сможете предотвратить многие конфликты.</p>
            <p class="c-font-red-3">Можно и нужно</p>
            <p>Собирать информацию. Узнайте о субкультуре, к которой примкнул подросток, что-то новое. И поделитесь этой информацией с ним. А лучше - займитесь совместным веб-серфингом: поищите нужную информацию в Интернете вдвоем. Это сблизит вас, а заодно и развеет многие накопившиеся страхи. Если вас все еще бросает в дрожь, посмотрите юмористический сериал "Папины дочки". Там есть спортсменка-футболистка, модница и даже готка! Они разные, со сложными характерами, но они - вместе и при этом прекрасно дополняют друг друга.</p>
            <p>Обсуждать все свои опасения и сомнения с подростком. Используйте при этом "Я-сообщения". Например: "Я беспокоюсь, что..." или "Меня волнует...", а не "Ты плохо себя ведешь", "Ты не прав". Не бойтесь спросить у подростка, если чего-то не знаете или не поняли из его объяснений. В таком разговоре важно не только умение аккуратно задавать вопросы, но и умение слушать, не перебивая.</p>
            <p>Быть внимательным и чутким. Важно помнить, что подростку бывает не менее страшно, больно или обидно, чем малышу. А ваше внимание, понимание и любовь в это сложное время жизни нужны ему даже больше, чем когда он был маленьким.</p>
            <p>Использовать увлечение подростка ему во благо. Он увлекается экстремальными видами спорта? Замечательно! Это повод поговорить о здоровом образе жизни. И теперь подобный разговор скорее всего не будет воспринят как нудные нотации и пустое сотрясание воздуха. Любит рок, особенно англоязычный? Тоже отлично! Практически любой подросток с удовольствием переведет и выучит текст песни, чтобы затем подпевать на концерте любимой группе. Приятное с полезным: и развлечется, и английский подтянет.</p>
            <p>Выбрать вместе с подростком атрибутику с символикой и названием его любимой группы (или спортивной команды) - это будет актом признания его права на самоопределение и самовыражение. А, придя домой, попросите его рассказать вам о любимых музыкантах или спортсменах. Подросток оценит ваше внимание, а вы наконец-то узнаете, почему "Ария" рулит!", а ""Зенит" - чемпион!".</p>
            <p>Рассказать о том, чем сами увлекались в молодости: этим вы покажете, что понимаете его страсть к музыке или спорту. Даже если подросток и назовет все это "нафталином", он поймет, что у вас с ним больше общего, чем различий.
                Любите их такими, какие они есть</p>
            <p>Важно помнить о том, что не бывает "опасных" или "безопасных" увлечений. Все зависит от конкретного человека: что одному полезно, то другому может оказаться вредно. Главное - общаться со своими детьми, быть в курсе их интересов. И почаще говорить (а лучше - доказывать на деле), что вы их любите. Любите и принимаете их всяких - со всеми их интересами и взглядами.</p>
            <p>А родители, в свою очередь, должны перепроверять информацию о субкультурах, которую им преподносят газеты, телевидение и Интернет. Тогда, в условиях взаимной ответственности, мифы постепенно исчезнут, и станет одним поводом меньше для споров отцов и детей.</p>
            <p>Как сказала одна 17-летняя девушка, бывшая в свое время и готом, и эмо: «Однажды придет осознание, что все этого больше не нужно. Родители обязательно должны разговаривать с ребенком о его увлечениях. Чтобы он не чувствовал себя одиноким. Девочка, например, может одеться в стиле гота или эмо не потому, что это отражает ее внутренний мир, а только потому, что стильно и модно».</p>
            <p>Наша родительская помощь и состоит в том, чтобы подросток произнес эти мудрые слова как можно раньше...Видимо, чем-то подобным надо переболеть, как ветрянкой в детстве. И только от нашей осведомленности, мудрости и такта зависит, оставит ли эта «ветрянка» следы на всю жизнь, и сколь глубокими будут эти следы.</p>
