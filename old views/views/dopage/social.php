            <p>О веществе насвай ходят разные слухи: говорят, что это надежное средство, чтобы бросить курить; считают его, то релаксантом, то видом табачного изделия, некурительным табаком для сосания.</p>
            <p>Название, вероятно, связано с тем, что раньше для его изготовления использовали растение "нас". По некоторым данным, основной компонент насвая - махорка или табак. В смесь добавляют гашеную известь, золу растений, верблюжий кизяк или куриный помет, иногда масло, а кое-где к навозу подмешивают сухофрукты и приправы. Есть данные и о другом составе насвая, когда табачную пыль, клей, известь, воду или растительное масло скатывают в шарики. Добавки держат форму, гранулируют пылеобразные отходы табачного производства.</p>
            <p>Фабричного насвая нет. Лепят его в домашних условиях - там, где находят нужное количество верблюжьего кизяка или куриного помета. Внешний вид у него разный - от зеленых шариков до серовато-коричневого, а иногда и почти чёрного порошка.</p>
            <p>Насвай дает высокий риск привыкания, рак ротовой полости и пищевода, заболевания желудочно-кишечного тракта.</p>
            <p>В практике отмечены также последствия краткосрочного и длительного воздействия насвая: сильное жжение слизистой ротовой полости, тяжесть в голове, а позднее - во всем теле; апатия, резкое слюноотделение, головокружение, расслабленность мышц.</p>
            <p>При сглатывании обильно выделяющейся слюны она или крупинки зелья вызывают тошноту, рвоту, понос. Именно рвота мучает начинающих потребителей. Бывалые перестают замечать жжение, неприятный запах и вкус, для них они - норма. В случае сочетания насвая с алкоголем, эффект непредсказуем.</p>
            <p>У насвая есть и ещё одна опасность. Экскременты животных, используемые в составе насвая, заражают кишечными инфекциями и паразитарными заболеваниями, в т. ч. вирусным гепатитом.</p>
            <p>Когда растение поливают неразбавленным куриным пометом, оно «сгорает». То же происходит и в организме человека: в первую очередь, «горят» слизистые рта и желудочно-кишечного тракта. Язва желудка обеспечена.</p>
            <p>От табака в насвае развивается никотиновая зависимость. Он разрушает зубы.</p>
            <p>Насвай можно отнести к числу психотропных веществ. Его употребление отражается на психическом развитии - снижается восприятие и ухудшается память, появляется неуравновешенность. «Насвайщики» не скрывают, что у них есть проблемы с памятью. Очень скоро изменяется личность потребителя насвая: нарушается психика, нервирует непроходящее состояние растерянности.</p>
            <p>Поскольку в насвае намешано всего, то у сосущих его может развиться зависимость не только никотиновая, но и от других химических веществ. Вскоре подростку хочется уже более сильных ощущений. Насвай заставляет перейти к наркотическим и другим, более сильным психотропным веществам.</p>
