                <li>Принцип диалогичности. Мир детства представляет собой особую ценность для взрослых: диалог двух миров (детей и взрослых) составляет учебно-воспитательный процесс, в котором обучение есть движение содержания мира взрослости в мир детства, а воспитание - движение содержания мира детства в мир взрослости.</li>
                <li>Принцип сосуществования. Мир детства и мир взрослых должны поддерживать обоюдный суверенитет, исходить из идеи невмешательства, не навязывания друг другу своих ценностей и законов. Любая акция взрослых или детей не должна наносить ущерб ни одной стороне.</li>
                <li>Принцип свободы. Предоставление миру детства полной свободы в выборе собственного пути. Взрослые обязаны сохранять жизнь и здоровье детей.</li>
                <li>Принцип единства. Мир детства и мир взрослых не образуют двух разграниченных миров, но составляют единый мир людей.</li>
                <li>Принцип принятия. Особенности любого человека должны приниматься другими людьми такими, каковы они есть. Отношения должны складываться только на основе безоговорочной любви. Ненависть может проявляться только в отношении поступка, а не человека, так как человек больше, чем его негативный поступок.</li>
