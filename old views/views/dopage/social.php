                <h2>Чем опасны курительные смеси?</h2>
                <p>Курительные смеси или курительные миксы - это ароматные сборы трав, предназначенные для использования в качестве комнатных благовоний и ароматизации помещений, однако используется данная продукция для употребления внутрь путем курения. Сегодня доказано, что большинство компонентов курительных смесей содержат наркотические вещества, которые губительно действуют на психику, обладают токсическим действием на органы и ткани, а их длительное употребление приводит к наркотической зависимости.</p>
                <p>Негативное действие на психику человека проявляется:</p>
                <ul>
                    <li>неспособностью сосредоточиться;</li>
                    <li>полной потерей контакта с окружающим миром;</li>
                    <li>отсутствием способности ориентироваться в пространстве;</li>
                    <li>потерей контроля над собственной личностью;</li>
                    <li>агрессией.</li>
                </ul>
                <p>Известны случаи, когда обкурившиеся подростки спрыгивали с 8-го этажа, срывали с себя одежду, выходили голыми на мороз. Но эти непосредственные реакции — не самое большое зло.</p>
                <p>Систематическое курение миксов приводит к необратимым процессам в центральной нервной системе. Снижается внимание, ухудшается память, замедляется мыслительная деятельность, человек теряет интерес к жизни.</p>
                <p>Практически все курильщики миксов жалуются на кашель, слёзотечение, осиплость горла во время и после курения. Велика вероятность возникновения злокачественных опухолей ротовой полости, глотки, гортани и бронхов.</p>
                <p>Продолжительное употребление курительных смесей по своей опасности превосходит даже тяжелые виды наркотиков. И медики, и эксперты в один голос утверждают, что даже разовое применение этих смесей может привести к смертельному исходу или тяжелой инвалидности.</p>
